<script>

import { ref, computed, onMounted } from 'vue';

export default {
  name:"Todo-list",

  setup() {
    const newTodo = ref('');
    const todos = ref([]);
    const filter = ref('all')
    const selected = ref('selected: undefined')
    const addNewTodo = () => {
     if (newTodo.value !== '') {
        todos.value.push({
        done: false,
        task: newTodo.value,
        id: Date.now(),
      });
      saveTodos();
    }
      newTodo.value = ''
    }

      const filteredTodos = computed(() => {
       const filterFn = filter.value === 'active'  ? (item) => !item.done :
       filter.value === 'completed' ? (item) => item.done : () => true;
       return todos.value.filter(filterFn)
     })


     onMounted(() => {
       if (localStorage.getItem('todos')) {
         try {
           todos.value = JSON.parse(localStorage.getItem('todos'));
         } catch(e) {
           localStorage.removeItem('todos')
         }
       }
     })
     

    const toggleDone = (todo) => {
      todo.done = !todo.done
      saveTodos();
    }

    const deleteTodo = (index) => {
      todos.value.splice(index, 1)
      saveTodos();
    }

/*     const body = document.body
    let theme = localStorage.getItem('theme');
  if (theme !== 'dark') theme = 'light' */
/*     const theme = localStorage.getItem('theme')
    if (theme ) {
        body.classList.add(theme) 
    } */

    const themeMap = {
      dark: 'light',
      light: 'dark',

    };

    let theme = localStorage.getItem('theme');
    const bodyClass = document.body.classList;
    if(theme === '') {theme = 'light'}
    bodyClass.add(theme);  
    const switchTheme = () => {
/*       if(body.classList.contains('light')){
      body.classList.replace('light', 'dark');
      localStorage.setItem('theme', 'dark')
     } else {
       body.classList.replace('dark', 'light');
       localStorage.setItem('theme', 'light')
     } */
     const current = localStorage.getItem('theme');
  const next = themeMap[current];

  bodyClass.replace(current, next);
  localStorage.setItem('theme', next);
    }

    const deleteCompleted = () => {
      todos.value = todos.value.filter(item => !item.done)
      saveTodos();
    }

    const saveTodos = () => {
      const parsed = JSON.stringify(todos.value);
      localStorage.setItem('todos', parsed);
    }

    return {
    addNewTodo,
    newTodo,
    todos,
    toggleDone,
    deleteTodo,
    switchTheme,
    filteredTodos,
    filter,
    deleteCompleted,
    saveTodos,
    todosLength:computed(()=>todos.value.filter(item => !item.done).length),
    selected


  }
  }
}
</script>

